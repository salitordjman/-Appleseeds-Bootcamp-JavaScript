// getAllCarToBuyByAgencyId: function (id) {} @return {Array} arrays of cars objects
carMarket.getAgencyById = function (id) {
  return this.sellers.find((agencys) => agencys.agencyId === id);
};

// getAllCarToBuyByAgencyId: function () {}, // @return {Array} - arrays of all models objects of specific agency
carMarket.getAllCarToBuyByAgencyId = function (id) {
  const result = [];
  const agencys = this.getAgencyById(id);
  agencys.cars.forEach((car, i) => {
    result.push(...car.models);
  });
  return result;
};

// getAllCarToBuyBy: function () {}, // @return {Array} - arrays of all models objects in all sellers
carMarket.getAllCarsToBuy = function () {
  const res = [];
  this.sellers.forEach((agency) => {
    res.push(...this.getAllCarToBuyByAgencyId(agency.agencyId));
  });
  return res;
};
